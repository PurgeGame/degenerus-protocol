function toBigInt(value) {
  return typeof value === "bigint" ? value : BigInt(value.toString());
}

async function collectTokensForPlayers(gamepieces, players, fromBlock) {
  const latestBlock = await hre.ethers.provider.getBlockNumber();
  const transfers = await gamepieces.queryFilter(gamepieces.filters.Transfer(), fromBlock, latestBlock);
  const ownership = new Map();

  for (const event of transfers) {
    const args = event.args || {};
    const tokenIdRaw = args.tokenId ?? args[2];
    const toRaw = args.to ?? args[1];
    if (tokenIdRaw === undefined || !toRaw) continue;

    const tokenKey = toBigInt(tokenIdRaw).toString();
    const to = toRaw.toLowerCase();
    if (to === ZERO_ADDRESS) {
      ownership.delete(tokenKey);
    } else {
      ownership.set(tokenKey, to);
    }
  }

  const baseTokenId = await gamepieces.currentBaseTokenId();
  const tokensByPlayer = new Map();
  for (const player of players) {
    tokensByPlayer.set(player.address.toLowerCase(), []);
  }

  for (const [tokenIdStr, owner] of ownership.entries()) {
    const tokenId = BigInt(tokenIdStr);
    if (tokenId < baseTokenId) continue;
    const list = tokensByPlayer.get(owner);
    if (list) list.push(tokenId);
  }

  for (const list of tokensByPlayer.values()) {
    list.sort((a, b) => (a < b ? -1 : a > b ? 1 : 0));
  }

  return { tokenIdsByPlayer: tokensByPlayer, latestBlock, baseTokenId };
}

