async function fulfillVrfIfRequested(vrfCoordinator, game) {
  const latestBlock = await hre.ethers.provider.getBlockNumber();
  const fromBlock = Math.max(latestBlock - 1, 0);
  const vrfRequestFilter = vrfCoordinator.filters.RandomWordsRequested();
  const events = await vrfCoordinator.queryFilter(vrfRequestFilter, fromBlock, latestBlock);

  if (events.length === 0) return false;

  const gameAddress = await game.getAddress();
  for (const event of events) {
    const requestId = event.args.requestId;
    const requester = event.args.sender;
    if (requester && requester.toLowerCase() !== gameAddress.toLowerCase()) {
      continue;
    }
    const request = await vrfCoordinator.s_requests(requestId);
    if (!request.fulfilled) {
      const randomWords = [BigInt("0x" + Math.floor(Math.random() * Number.MAX_SAFE_INTEGER).toString(16))];
      await vrfCoordinator.fulfillRandomWords(requestId, gameAddress, randomWords);
      console.log(`    рџЋІ VRF fulfilled: request ${requestId}`);
    }
  }

  return true;
}

