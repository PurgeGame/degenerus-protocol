function parseArgs(argv) {
  const out = {};
  for (let i = 0; i < argv.length; i += 1) {
    const raw = argv[i];
    if (!raw.startsWith("--")) continue;
    const trimmed = raw.slice(2);
    const eqIdx = trimmed.indexOf("=");
    if (eqIdx !== -1) {
      const key = trimmed.slice(0, eqIdx);
      const value = trimmed.slice(eqIdx + 1);
      out[key] = value;
      continue;
    }
    const next = argv[i + 1];
    if (next && !next.startsWith("--")) {
      out[trimmed] = next;
      i += 1;
    } else {
      out[trimmed] = true;
    }
  }
  return out;
}

function requireAddress(label, value) {
  if (!value || typeof value !== "string" || !isAddress(value)) {
    throw new Error(`Invalid ${label} address: ${value}`);
  }
  return value;
}

function parseNonce(value) {
  if (value === undefined) return null;
  const n = Number.parseInt(value, 10);
  if (!Number.isInteger(n) || n < 0) {
    throw new Error(`Invalid startNonce: ${value}`);
  }
  return n;
}

function renderSolidity(constants) {
  const lines = [
    "// SPDX-License-Identifier: MIT",
    "pragma solidity ^0.8.26;",
    "",
    "// AUTO-GENERATED by scripts/deploy/deploy-sepolia-testnet.js",
    "// Do not edit by hand; regenerate via the script.",
    "library ContractAddresses {",
    "    /// @notice Cost divisor for testnet (all ETH amounts divided by this)",
    "    /// @dev Set to 1_000_000 for testnet, 1 for mainnet",
    "    uint256 internal constant COST_DIVISOR = 1_000_000;",
    ""
  ];

  for (const name of ALL_CONSTANTS) {
    if (name === "VRF_KEY_HASH") {
      const value = constants[name] || ZERO_BYTES32;
      lines.push(`    bytes32 internal constant ${name} = ${value};`);
    } else {
      const value = constants[name] || ZERO_ADDRESS;
      lines.push(`    address internal constant ${name} = ${value};`);
    }
  }

  lines.push("}");
  lines.push("");
  return lines.join("\n");
}

async function deployWithCheck(factory, expected, label, args = [], options = {}) {
  const contract = await factory.deploy(...args, options);
  await contract.waitForDeployment();
  const addr = await contract.getAddress();
  if (addr.toLowerCase() !== expected.toLowerCase()) {
    throw new Error(`${label} deployed at ${addr}, expected ${expected}`);
  }
  return contract;
}

