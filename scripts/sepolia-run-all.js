async function runScript(scriptPath, description, env = {}) {
  console.log(`\n=== ${description} ===\n`);

  return new Promise((resolve, reject) => {
    const proc = spawn('npx', ['hardhat', 'run', scriptPath, '--network', 'sepolia'], {
      stdio: 'inherit',
      env: { ...process.env, ...env }
    });

    proc.on('close', (code) => {
      if (code === 0) {
        console.log(`\nвњ… ${description} completed\n`);
        resolve();
      } else {
        console.log(`\nвќЊ ${description} failed with code ${code}\n`);
        reject(new Error(`${description} failed`));
      }
    });
  });
}

